// Helper function to merge sample packs into the main DICT object
export function mergeSamplePacks(mainDict) {
  for (const [lang, pack] of Object.entries(SAMPLE_TRANSLATION_PACKS)) {
    if (!mainDict[lang]) {
      mainDict[lang] = {};
    }
    // Deep merge the pack into the main dictionary
    const mergeDeep = (target, source) => {
      for (const key in source) {
        if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
          if (!target[key]) target[key] = {};
          mergeDeep(target[key], source[key]);
        } else {
          target[key] = source[key];
        }
      }
    };
    mergeDeep(mainDict[lang], pack);
  }
  return mainDict;
}
